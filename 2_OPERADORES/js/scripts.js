// 1 -Number
console.log(typeof 2);
console.log(typeof 5.14);
console.log(typeof -27);

// 2 - Operacoes aritimeticas
console.log(5+6);
console.log(5-6);
console.log(5*6);
console.log(5/6);

// 3 - Special Number
console.log(typeof Infinity);
console.log(typeof -Infinity);
console.log(12 * "asd");
console.log(typeof NaN);

// 4 - Strings
console.log("Ola jovem");
console.log(`Certo jovem`);
console.log(`1`);

console.log(typeof "Ola jovem");
console.log(typeof `Certo jovem`);
console.log(typeof `1`);

// 5 - Simbolos especiais em string
console.log("Ola jovem \n tudo certo");
console.log(`Certo jovem \n tem coragem`);
console.log(`1 \n 2`);